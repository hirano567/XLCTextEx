bool GetBlockIdByName(int* pid, UCPSTRING* pucsName)
{
	UINT	uiIndex;

	if (PLib::BinarySearch<PSPropertyValueIdMap, UCPSTRING*>(
		&uiIndex, siBlockIdMap, nBlockIdMap, pucsName))
	{
		*pid	= siBlockIdMap[uiIndex].iId;
		return true;
	}
	return false;
}

bool GetBlockIdByName(int* pid, const T_UCP* pName, size_t zName)
{
	UCPSTRING	ucsTemp;

	ucsTemp.assign(pName, zName);
	return GetBlockIdByName(pid, &ucsTemp);
}

bool GetBlockIdByName(int* pid, const T_UCP* pzName)
{
	UCPSTRING	ucsTemp;

	ucsTemp.assign(pzName);
	return GetBlockIdByName(pid, &ucsTemp);
}

